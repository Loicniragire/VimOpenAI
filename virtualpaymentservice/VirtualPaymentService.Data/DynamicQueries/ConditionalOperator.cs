namespace VirtualPaymentService.Data.DynamicQueries
{
    public static class ConditionalOperator
    {
        /// <summary>
        /// IN
        /// </summary>
        public const string IN = "IN";

        /// <summary>
        /// LIKE
        /// </summary>
        public const string LIKE = "LIKE";

        /// <summary>
        /// Greater than
        /// </summary>
        public const string GT = ">";

        /// <summary>
        /// Greater than or equal to
        /// </summary>
        public const string GTEQ = ">=";

        /// <summary>
        /// Less than
        /// </summary>
        public const string LT = "<";

        /// <summary>
        /// Less than or equal to
        /// </summary>
        public const string LTEQ = "<=";

        /// <summary>
        /// Equal to
        /// </summary>
        public const string EQ = "=";
    }
}
